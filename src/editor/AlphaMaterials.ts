for (const [id, state] of Object.entries(alphaMaterials)) {
	const [block, data] = id.split(':').map(s => Number(s))
	const dataMap = computeIfAbsent(blockMap, block, () => new Map<number, string>())
	computeIfAbsent(dataMap, data, () => state)
}

export function fromAlphaMaterial(block: number, data: number) {
	const str = blockMap.get(block)?.get(data) ?? 'air'
	return BlockState.parse(str)
}
